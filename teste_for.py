print("-----------for-in-------------")
frutas = ["Uva", "Abacate", "Laranja", "Melancia"]
for x in range(len(frutas)):
    print(frutas[x])

print("--------for-in-range----------")
for i in range(10):
    print(f"{i} - Teste")

print("-----------while--------------")
i = 0
while i < 0:
    print(f"{i} -- Teste")
    i = i + 1

